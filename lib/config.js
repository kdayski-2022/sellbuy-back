const {
  SERVICE_WALLET_ADDRESS,
  PAYOUT_CONTRACT_ADDRESS,
  CHAIN_NAMES,
  CHAIN_LIST,
  CHAIN_GAS_LIMITS,
  WITHDRAWAL_TOKEN_ADDRESS,
  INFURA_PROVIDERS,
  BLOCK_EXPLORERS,
  PAYIN_TOKEN_ADDRESS_LIST,
  DECIMALS,
  TOKEN_ADDRESS,
  CHAIN_LIST_ENV,
} = require('../config/network');

const getConfigByEnv = (env) => {
  let SWA,
    PCA,
    CN,
    CGP,
    CL,
    IP,
    BE,
    PTAL,
    D,
    TA,
    CLE,
    WTA = null;
  switch (env) {
    case 'production':
      SWA = {
        1: SERVICE_WALLET_ADDRESS[1],
        42161: SERVICE_WALLET_ADDRESS[42161],
      };
      PCA = {
        1: PAYOUT_CONTRACT_ADDRESS[1],
        42161: PAYOUT_CONTRACT_ADDRESS[42161],
      };
      CN = {
        1: CHAIN_NAMES[1],
        42161: CHAIN_NAMES[42161],
      };
      CGP = {
        1: CHAIN_GAS_LIMITS[1],
        42161: CHAIN_GAS_LIMITS[42161],
      };
      CL = [1, 42161];
      WTA = {
        1: WITHDRAWAL_TOKEN_ADDRESS[1],
        42161: WITHDRAWAL_TOKEN_ADDRESS[42161],
      };
      IP = {
        1: INFURA_PROVIDERS[1],
        42161: INFURA_PROVIDERS[42161],
      };
      BE = {
        1: BLOCK_EXPLORERS[1],
        42161: BLOCK_EXPLORERS[42161],
      };
      PTAL = {
        1: PAYIN_TOKEN_ADDRESS_LIST[1],
        42161: PAYIN_TOKEN_ADDRESS_LIST[42161],
      };
      D = DECIMALS;
      CLE = CHAIN_LIST_ENV['production'];
      TA = {
        1: TOKEN_ADDRESS[1],
        42161: TOKEN_ADDRESS[42161],
      };
      break;
    case 'development':
      SWA = {
        1: SERVICE_WALLET_ADDRESS[1],
        42161: SERVICE_WALLET_ADDRESS[42161],
        80001: SERVICE_WALLET_ADDRESS[80001],
        421613: SERVICE_WALLET_ADDRESS[421613],
      };
      PCA = {
        1: PAYOUT_CONTRACT_ADDRESS[1],
        42161: PAYOUT_CONTRACT_ADDRESS[42161],
        80001: PAYOUT_CONTRACT_ADDRESS[80001],
        421613: PAYOUT_CONTRACT_ADDRESS[421613],
      };
      CN = {
        1: CHAIN_NAMES[1],
        42161: CHAIN_NAMES[42161],
        80001: CHAIN_NAMES[80001],
        421613: CHAIN_NAMES[421613],
      };
      CGP = {
        1: CHAIN_GAS_LIMITS[1],
        42161: CHAIN_GAS_LIMITS[42161],
        80001: CHAIN_GAS_LIMITS[80001],
        421613: CHAIN_GAS_LIMITS[421613],
      };
      CL = [1, 42161, 80001, 421613];
      WTA = {
        1: WITHDRAWAL_TOKEN_ADDRESS[1],
        42161: WITHDRAWAL_TOKEN_ADDRESS[42161],
        80001: WITHDRAWAL_TOKEN_ADDRESS[80001],
        421613: WITHDRAWAL_TOKEN_ADDRESS[421613],
      };
      IP = {
        1: INFURA_PROVIDERS[1],
        42161: INFURA_PROVIDERS[42161],
        80001: INFURA_PROVIDERS[80001],
        421613: INFURA_PROVIDERS[421613],
      };
      BE = {
        1: BLOCK_EXPLORERS[1],
        42161: BLOCK_EXPLORERS[42161],
        80001: BLOCK_EXPLORERS[80001],
        421613: BLOCK_EXPLORERS[421613],
      };
      PTAL = {
        1: PAYIN_TOKEN_ADDRESS_LIST[1],
        42161: PAYIN_TOKEN_ADDRESS_LIST[42161],
        80001: PAYIN_TOKEN_ADDRESS_LIST[80001],
        421613: PAYIN_TOKEN_ADDRESS_LIST[421613],
      };
      D = DECIMALS;
      CLE = CHAIN_LIST_ENV['development'];
      TA = {
        1: TOKEN_ADDRESS[1],
        42161: TOKEN_ADDRESS[42161],
        80001: TOKEN_ADDRESS[80001],
        421613: TOKEN_ADDRESS[421613],
      };
      break;

    default:
      SWA = SERVICE_WALLET_ADDRESS;
      PCA = PAYOUT_CONTRACT_ADDRESS;
      CN = CHAIN_NAMES;
      CL = CHAIN_LIST;
      CGP = CHAIN_GAS_LIMITS;
      WTA = WITHDRAWAL_TOKEN_ADDRESS;
      IP = INFURA_PROVIDERS;
      BE = BLOCK_EXPLORERS;
      PTAL = PAYIN_TOKEN_ADDRESS_LIST;
      D = DECIMALS;
      CLE = CHAIN_LIST_ENV;
      TA = TOKEN_ADDRESS;
      break;
  }
  return {
    SERVICE_WALLET_ADDRESS: SWA,
    PAYOUT_CONTRACT_ADDRESS: PCA,
    CHAIN_NAMES: CN,
    CHAIN_LIST: CL,
    CHAIN_GAS_LIMITS: CGP,
    WITHDRAWAL_TOKEN_ADDRESS: WTA,
    INFURA_PROVIDERS: IP,
    BLOCK_EXPLORERS: BE,
    PAYIN_TOKEN_ADDRESS_LIST: PTAL,
    DECIMALS: D,
    CHAIN_LIST_ENV: CLE,
    TOKEN_ADDRESS: TA,
  };
};

module.exports = {
  getConfigByEnv,
};
